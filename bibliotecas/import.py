# Em Python, para usar as funcionalidades de uma biblioteca ou módulo, 
# é necessário importá-los no início do código. Existem algumas formas de fazer isso:

# import nome_da_biblioteca: essa é a forma mais comum de importar uma biblioteca em Python. 
# Ela importa a biblioteca inteira e todas as suas funções e classes são acessadas usando o nome da biblioteca como prefixo. 
import math

x = math.sqrt(25)
print(x)
# Saída: 5.0
