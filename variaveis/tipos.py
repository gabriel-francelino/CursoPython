# Tipos de variáveis em python

# Numéricos
idade = 32
preco = 49.99

# Strings
nome = "Maria"
mensagem = "Olá, mundo!"

# Booleanas
tem_chuva = True
tem_sol = False

# Listas
numeros = [1, 2, 3, 4, 5]
frutas = ["maçã", "banana", "laranja"]

# Dicionário
pessoa = {"nome": "João", "idade": 25, "cidade": "Rio de Janeiro"}
estoque = {"maçãs": 10, "laranjas": 5, "bananas": 20}

# Tupla
coordenadas = (10, 20)
cores = ("vermelho", "verde", "azul")

# Conjunto
numeros = {1, 2, 3, 4, 5}
letras = {"a", "b", "c", "d"}
