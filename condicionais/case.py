# Em Python, não existe uma estrutura de controle de fluxo switch case como em algumas outras linguagens de programação, como C e Java. 
# Em vez disso, pode-se usar uma série de declarações if/elif para simular o comportamento do switch case.
opcao = 3

if opcao == 1:
    print("Opção 1 selecionada")
elif opcao == 2:
    print("Opção 2 selecionada")
elif opcao == 3:
    print("Opção 3 selecionada")
else:
    print("Opção inválida")
