# Operações aritméticas:

# + (adição)
# - (subtração)
# * (multiplicação)
# / (divisão)
# % (resto da divisão)
# ** (potenciação)

a = 10
b = 3
print(a + b)   # Saída: 13
print(a - b)   # Saída: 7
print(a * b)   # Saída: 30
print(a / b)   # Saída: 3.3333333333333335
print(a % b)   # Saída: 1
print(a ** b)  # Saída: 1000
