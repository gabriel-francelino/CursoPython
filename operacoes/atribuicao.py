# Operações de atribuição:

# = (atribuição simples)
# += (adição e atribuição)
# -= (subtração e atribuição)
# *= (multiplicação e atribuição)
# /= (divisão e atribuição)
# %= (resto da divisão e atribuição)
# **= (potenciação e atribuição)

x = 5
x += 2  # equivalente a x = x + 2
print(x)   # Saída: 7

y = 10
y -= 3  # equivalente a y = y - 3
print(y)   # Saída: 7

z = 3
z *= 4  # equivalente a z = z * 4
print(z)   # Saída: 12

w = 15
w /= 2  # equivalente a w = w / 2
print(w)   # Saída: 7.5

v = 20
v %= 3  # equivalente a v = v % 3
print(v)   # Saída: 2

u = 2
u **= 3  # equivalente a u = u ** 3
print(u)   # Saída: 8
