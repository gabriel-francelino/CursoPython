# Operações de comparação:

# == (igual)
# != (diferente)
# < (menor que)
# > (maior que)
# <= (menor ou igual a)
# >= (maior ou igual a)

a = 10
b = 3
print(a == b)   # Saída: False
print(a != b)   # Saída: True
print(a < b)    # Saída: False
print(a > b)    # Saída: True
print(a <= b)   # Saída: False
print(a >= b)   # Saída: True
