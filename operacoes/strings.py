# Operações com strings:

# + (concatenação)
# * (repetição)
# [] (indexação)
# [:] (fatiamento)
# len() (tamanho)

a = "Olá"
b = "Mundo"
print(a + " " + b)   # Saída: "Olá Mundo"
print(b * 3)   # Saída: "MundoMundoMundo"
print(a[1])    # Saída: "l"
print(b[1:3])  # Saída: "un"
print(len(a))  # Saída: 3
